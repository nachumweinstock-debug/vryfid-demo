const BUDGET_RANGES = {
  under15: [0, 14999],
  "15to22": [15000, 22000],
  "22to35": [22001, 35000],
  "35plus": [35001, Infinity],
};

export function scoreAndRank(answers) {
  return [...LISTINGS]
    .map((l) => {
      let pts = 0;

      // Budget — 0–4 pts
      const [lo, hi] = BUDGET_RANGES[answers.budget] ?? [0, Infinity];
      if (l.priceNum >= lo && l.priceNum <= hi) {
        pts += 4;
      } else if (l.priceNum < lo && lo - l.priceNum <= 5000) {
        pts += 2;
      } else if (l.priceNum > hi && l.priceNum - hi <= 8000) {
        pts += 2;
      }

      // Priority — 0–3 pts
      if (l.priorities[0] === answers.priority) pts += 3;
      else if (l.priorities[1] === answers.priority) pts += 1;

      // Location — 0–3 pts
      if (l.area === answers.location) pts += 3;

      return { ...l, score: pts };
    })
    .sort((a, b) => b.score - a.score);
}
